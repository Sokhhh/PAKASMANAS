import java.awt.Color;

/**
 * Contains the color used in the application.
 *
 * @version 1.0
 */
public class PacmanTheme {

    /** Wall color in the game. */
    public static Color WALL = Color.BLACK;

    /** Text color in the start interface. */
    public static Color WELCOME_TEXT = Color.WHITE;

    /** Background color in the game. */
    public static Color BACKGROUND = Color.WHITE;

    /** Text color in the game. */
    public static Color GAME_TEXT = Color.BLACK;

    /** Background color in the start interface. */
    public static Color WELCOME_BACKGROUND = Color.BLACK;

    /** Color of a button when the mouse is hovering over. */
    public static Color BUTTON_HOVERED = new Color(110, 110, 110);

    /** Color of a button when the mouse is pressed on. */
    public static Color BUTTON_PRESSED = new Color(189, 189, 189);
}
